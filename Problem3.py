#Get two number from user and do arithmatic calculations

a = int(input("Enter the first number: "))
b = int(input("Enter the second number: "))
add = a + b
sub = a - b
mul = a * b
div = a / b
fdiv = a // b
pow = a ** b
print(f"Add is {add}, sub is {sub}, Multiplication is {mul}, division is {div}, Floor division is {fdiv} and Power is {pow}")
